# Tests
# =============================================================================

def test_fbm_spot_check():
    """
    Verify that scaleinvariance.fBm_ND_circulant produces the expected values.

    If this fails, the fBm generator itself has changed. All downstream test
    failures should be attributed to scaleinvariance, not objscale.
    """
    print("\n=== Test: fBm spot-check ===")
    failures = []

    for H in H_VALUES:
        field = _generate_raw_field(H)
        expected = FBM_SPOT_VALUES[H]
        for k, (i, j) in enumerate(FBM_SPOT_PIXEL_LOCS):
            actual = float(field[i, j])
            if abs(actual - expected[k]) > ATOL:
                failures.append(f"H={H}, pixel ({i},{j}): expected {expected[k]}, got {actual}")

    if failures:
        for f in failures:
            print(f"  FAIL  {f}")
        raise AssertionError(
            f"fBm spot-check failed ({len(failures)} mismatches). "
            "scaleinvariance.fBm_ND_circulant may have changed — "
            "regenerate reference data with --regen if the new values are correct."
        )
    print(f"  PASS  All {len(H_VALUES) * len(FBM_SPOT_PIXEL_LOCS)} spot values match")


def test_fractal_exact_props():
    """
    Verify get_structure_props returns analytically exact values for recursive fractals.

    All structures are perfect squares, so area, perimeter, height, width, and
    count per scale class are exactly known. No tolerance needed.
    """
    print("\n=== Test: fractal exact structure properties ===")
    failures = []

    for seed_name, iter_dict in FRACTAL_EXACT.items():
        for iterations, expected_groups in iter_dict.items():
            fractal = generate_fractal(seed=seed_name, iterations=iterations)
            binary = objscale.encase_in_value(fractal.astype(np.float32), value=0, dtype=np.float32)
            x_sizes = np.ones_like(binary, dtype=np.float32)
            y_sizes = np.ones_like(binary, dtype=np.float32)

            p, a, h, w = objscale.get_structure_props(binary, x_sizes, y_sizes)
            label = f"{seed_name}/iter={iterations}"

            # Check total structure count
            expected_total = sum(eg[4] for eg in expected_groups)
            if len(a) != expected_total:
                failures.append(f"{label}: expected {expected_total} structures, got {len(a)}")
                continue

            # Check each scale class
            for exp_a, exp_p, exp_h, exp_w, exp_count in expected_groups:
                mask = a == exp_a
                count = np.sum(mask)
                if count != exp_count:
                    failures.append(f"{label}: area={exp_a}, expected count={exp_count}, got {count}")
                    continue

                # All structures of this size should have identical properties
                for prop_name, prop_arr, exp_val in [
                    ('perimeter', p, exp_p), ('height', h, exp_h), ('width', w, exp_w),
                ]:
                    vals = prop_arr[mask]
                    if not np.all(vals == exp_val):
                        failures.append(
                            f"{label}: area={exp_a}, {prop_name} expected {exp_val}, "
                            f"got {np.unique(vals)}"
                        )

            if label not in str(failures):
                print(f"  PASS  {label} ({len(a)} structures, {len(expected_groups)} scale classes)")

    if failures:
        for f in failures:
            print(f"  FAIL  {f}")
        raise AssertionError(f"Fractal exact props: {len(failures)} failure(s)")
    print("  All fractal exact tests passed.")


def test_structure_props_regression():
    """
    Verify get_structure_props on seeded fBm fields matches saved reference data.

    Outputs are sorted by (area, perimeter) before comparison so that structure
    ordering does not matter.
    """
    print("\n=== Test: get_structure_props regression ===")
    failures = []

    for H in H_VALUES:
        binary = _generate_field(H)
        for config in PIXEL_SIZE_CONFIGS:
            x_sizes, y_sizes = _make_sizes(binary, config)
            p, a, h, w = objscale.get_structure_props(binary, x_sizes, y_sizes)
            p_sorted, a_sorted = _sort_props(p, a)

            ref_path = os.path.join(REFERENCE_DIR, f'props_H{H}_{config}.npz')
            if not os.path.exists(ref_path):
                raise FileNotFoundError(
                    f"Reference not found: {ref_path}\nRun with --regen first."
                )
            ref = np.load(ref_path)
            label = f"H={H}/{config}"

            if len(a_sorted) != len(ref['areas']):
                failures.append(f"{label}: count {len(a_sorted)} != ref {len(ref['areas'])}")
                continue

            for prop_name, current, ref_key in [
                ('areas', a_sorted, 'areas'),
                ('perimeters', p_sorted, 'perimeters'),
            ]:
                if not np.allclose(current, ref[ref_key], rtol=RTOL, atol=ATOL):
                    diffs = ~np.isclose(current, ref[ref_key], rtol=RTOL, atol=ATOL)
                    max_diff = np.max(np.abs(current - ref[ref_key]))
                    failures.append(
                        f"{label}/{prop_name}: {np.sum(diffs)} values differ, "
                        f"max diff={max_diff:.6e}"
                    )
                else:
                    print(f"  PASS  {label}/{prop_name} ({len(current)} structures)")

    if failures:
        for f in failures:
            print(f"  FAIL  {f}")
        raise AssertionError(f"Structure props regression: {len(failures)} failure(s)")
    print("  All structure props regression tests passed.")


def test_size_distribution_regression():
    """
    Verify finite_array_size_distribution on seeded fBm fields matches saved references.
    """
    print("\n=== Test: size distribution regression ===")
    failures = []

    for H in [0.3, 0.7]:
        binary = _generate_field(H, pad=False)  # no zero-pad: test truncation path
        for config in PIXEL_SIZE_CONFIGS:
            x_sizes, y_sizes = _make_sizes(binary, config)
            for variable in ['area', 'perimeter']:
                bins_mid, nontrunc, trunc, trunc_idx = objscale.finite_array_size_distribution(
                    binary, variable=variable, x_sizes=x_sizes, y_sizes=y_sizes,
                    bins=100, min_threshold=2,
                )

                ref_path = os.path.join(
                    REFERENCE_DIR, f'sizedist_{variable}_H{H}_{config}.npz'
                )
                if not os.path.exists(ref_path):
                    raise FileNotFoundError(
                        f"Reference not found: {ref_path}\nRun with --regen first."
                    )
                ref = np.load(ref_path)
                label = f"H={H}/{config}/{variable}"

                for arr_name, current, ref_key in [
                    ('bins_mid', bins_mid, 'bins_mid'),
                    ('nontrunc', nontrunc, 'nontrunc'),
                    ('trunc', trunc, 'trunc'),
                    ('trunc_idx', np.array(trunc_idx), 'trunc_idx'),
                ]:
                    cur = current.astype(float)
                    ref_arr = ref[ref_key].astype(float)
                    if not np.allclose(cur, ref_arr, rtol=RTOL, atol=ATOL):
                        max_diff = np.max(np.abs(cur - ref_arr))
                        failures.append(f"{label}/{arr_name}: max diff={max_diff:.6e}")

                if label not in str(failures):
                    print(f"  PASS  {label}")

    if failures:
        for f in failures:
            print(f"  FAIL  {f}")
        raise AssertionError(f"Size distribution regression: {len(failures)} failure(s)")
    print("  All size distribution regression tests passed.")


