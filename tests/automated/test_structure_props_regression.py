if __name__ == '__main__':
    parser = argparse.ArgumentParser(
        description="Regression tests for get_structure_props refactor."
    )
    parser.add_argument('--regen', action='store_true',
                        help='Regenerate reference data (run this once before testing)')
    args = parser.parse_args()

    if args.regen:
        generate_reference_data()
        sys.exit(0)

    if not os.path.exists(REFERENCE_DIR):
        print("No reference data found. Generating...")
        generate_reference_data()

    passed = 0
    failed = 0
    tests = [
        test_fbm_spot_check,
        test_fractal_exact_props,
        test_structure_props_regression,
        test_size_distribution_regression,
    ]
    for test_func in tests:
        try:
            test_func()
            passed += 1
        except AssertionError as e:
            print(f"\n*** {e}")
            failed += 1
        except Exception as e:
            print(f"\n*** ERROR in {test_func.__name__}: {e}")
            failed += 1

    print(f"\n{'=' * 60}")
    print(f"SUMMARY: {passed} passed, {failed} failed")
    print(f"{'=' * 60}")
    sys.exit(0 if failed == 0 else 1)
